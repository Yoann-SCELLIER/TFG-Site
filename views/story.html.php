    <h1>TF - True Fighters Gaming</h1>
    <p>True Fighters Gaming (TF) est une association dynamique et inclusive, ancrée dans une passion commune pour
        les jeux vidéo.<br>
        Fondée en 2012 sur la plateforme Xbox 360, la TF a débuté sous la licence Halo.<br>
        Rapidement, l'équipe a gagné en popularité et s'est agrandie, menant à une évolution naturelle vers une
        structure plus formelle.<br>
        <br>
        En 2015, la TF est devenue une association officielle sous le statut de la loi 1901.<br>
        True Fighters Gaming repose sur des principes fondamentaux de fair-play, d'entraide, de plaisir et d'esprit
        d'équipe.<br>
        L'association est libre et sans frais d'adhésion, mais elle exige de ses membres qu'ils véhiculent une image
        positive et maintiennent<br>
        une réputation respectable pour l'ensemble de la communauté.<br>
        Ces valeurs sont au cœur de toutes les activités de la TF, garantissant un environnement accueillant et
        respectueux pour tous.<br>
        Aujourd'hui, True Fighters Gaming est présente sur plusieurs plateformes, notamment Xbox Série, PC et
        Smartphone, permettant aux membres<br>
        de se retrouver et de jouer ensemble, peu importe leur préférence en matière de matériel.<br>
        Que ce soit à travers des aventures en solo, des parties en ligne en équipe ou des compétitions amicales,
        les membres de la TF trouvent toujours<br>
        une manière de se connecter et de partager leur passion.<br>
        <br>
        True Fighters Gaming est plus qu'une simple association de joueurs ; c'est une communauté vibrante où chaque
        membre trouve sa place.<br>
        L'association est active sur les réseaux sociaux, avec des pages sur Facebook, YouTube, Twitter et Discord,
        permettant aux membres de rester<br>
        connectés et informés des dernières actualités et événements.<br>
        <br>
        Pour toute question, suggestion ou demande de partenariat, la TF peut être contactée.<br>
        L'association est toujours prête à écouter et à collaborer avec ceux qui partagent sa passion pour les jeux
        vidéo.<br>
        Depuis ses débuts en 2012, la TF a évolué pour devenir une association respectée et aimée, rassemblant des
        joueurs de tous horizons dans une aventure<br>
        collective et passionnante.
    </p>
